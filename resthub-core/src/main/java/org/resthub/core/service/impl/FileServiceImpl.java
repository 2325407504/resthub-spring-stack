	 /**
	  * {@inheritDoc}
	  */
	public String getMimeTypeFromFileName(String fileName) {
		// check if filename argument is not null
		if (fileName == null) {
			logger.error("Error in getMimeTypeFromFileName : fileName cannot be null !!");
			throw new IllegalArgumentException("fileName cannot be null !!");
		}
		
		// chack if filename contains an extension
		String[] tokens = fileName.split("\\.");
		if ((0 == tokens.length) || (1 == tokens.length)) {
			logger.error("Error in getMimeTypeFromFileName : fileName does not contain any extension");
			return null;
		}
		
		// get corresponding mime type
		String extension = tokens[tokens.length - 1];
		String resultMimeType = mimetypes.get(extension);
		
		if (null == resultMimeType) {
			logger.error("Error in getMimeTypeFromFileName : no mime type corresponding to extension: "+extension);
		}
		
		return resultMimeType;
	}

	
